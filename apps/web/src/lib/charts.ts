export const getEngagementColor = (state: string): string => {
  const colors = {
    engaged: '#10b981',
    distracted: '#f97316', 
    sleepy: '#3b82f6',
    away: '#ef4444',
  };
  return colors[state as keyof typeof colors] || '#6b7280';
};